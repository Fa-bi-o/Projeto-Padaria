    private void carregarUsuarios() {
        listaModelClientes = IControllerUsuario.getListausuarioController();
        DefaultTableModel modelo = (DefaultTableModel) tabelaCliente.getModel();
        modelo.setNumRows(0);

        int cont = listaModelClientes.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelClientes.get(i).getIdUser(),
                listaModelClientes.get(i).getNome(),
                listaModelClientes.get(i).getCidade(),
                listaModelClientes.get(i).getTelefone()
            });
        }
    }

