    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (salvarAlterar.equals("salvar")) {
            modelUsuario.setNome(txtNome.getText());
            modelUsuario.setMatricula(Integer.parseInt(txtMatricula.getText()));
            modelUsuario.setCpf(txtCpf.getText());
            modelUsuario.setEndereco(txtEndereco.getText());
            modelUsuario.setBairro(txtBairro.getText());
            modelUsuario.setCidade(txtCidade.getText());
            modelUsuario.setCep(txtCep.getText());
            modelUsuario.setTelefone(txtTelefone.getText());
            modelUsuario.setLogin(txtLogin.getText());
            modelUsuario.setSenha(txtSenha.getText());
            modelUsuario.setEstado(cbEstado.getSelectedItem().toString());
            modelUsuario.setFuncao(cbFuncao.getSelectedIndex());

            if (IControllerUsuario.salvarusuarioController(modelUsuario) > 0) {
                JOptionPane.showMessageDialog(this, "Usuário Salvo com Sucesso", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                carregarUsuarios();
                desabilitaCampos(false);
                limparCampos();

            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar usuário", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            modelUsuario.setIdUser(Integer.parseInt(txtCodigo.getText()));
            modelUsuario.setNome(txtNome.getText());
            int conversor = 0;
            try {
                conversor = Integer.parseInt(txtMatricula.getText());
            } catch (Exception e) {
                e.printStackTrace();
            }
            modelUsuario.setMatricula(conversor);
            modelUsuario.setCpf(txtCpf.getText());
            modelUsuario.setEndereco(txtEndereco.getText());
            modelUsuario.setBairro(txtBairro.getText());
            modelUsuario.setCidade(txtCidade.getText());
            modelUsuario.setCep(txtCep.getText());
            modelUsuario.setTelefone(txtTelefone.getText());
            modelUsuario.setEstado(cbEstado.getSelectedItem().toString());
            modelUsuario.setFuncao(cbFuncao.getSelectedIndex());

            if (IControllerUsuario.atualizarusuarioController(modelUsuario)) {
                JOptionPane.showMessageDialog(this, "Usuario Salvo com Sucesso", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                carregarUsuarios();

            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar Usuário", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_btnSalvarActionPerformed

